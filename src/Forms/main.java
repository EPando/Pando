    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        try{
            int n=Integer.parseInt(trapecios.getText());
            double res= n%2;
            if(n==0){
                JOptionPane.showMessageDialog(resultado, "Verifique que el número de segmentos este correctamente configurado (no 0).");
            }
            if(n==1){
                Trapecio trapecio= new Trapecio();
                trapecio.CalcularIntegral(funcion.getText(),limb.getText(),lima.getText());
                resultado.setText(trapecio.getArea());
            }
            if((n>1) && (res==0)){
                Trapecio trapecio= new Trapecio();
                trapecio.CalcularIntegral(funcion.getText(),limb.getText(),lima.getText());
                resultado.setText(trapecio.getArea());

                double b=Double.parseDouble(limb.getText());
                double a=Double.parseDouble(lima.getText());
                double suma= ((b-a)/n);
                double ne= suma+ Double.parseDouble(lima.getText());
                String total= "0";
                String first= lima.getText();
                String next= String.valueOf(ne);
                for(int i=1;i<=n;i++){
                    trapecio.CalcularIntegral(funcion.getText(), next, first);
                    first= next;
                    double sum= suma+ Double.parseDouble(first);
                    next= String.valueOf(sum);
                    total=Double.parseDouble(trapecio.getArea())+ Double.parseDouble(String.valueOf(total))+"".trim();
                    resultado.setText("");
                    resultado.setText(total);
                }
            }
            else{
                JOptionPane.showMessageDialog(resultado, "\"Verifique que el número de segmentos este correctamente configurado (impar).\"", "ATENCION", 2);
            }
        }catch(NumberFormatException | HeadlessException e){
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error.\nVerifique que su ecuacion sea correcta y no deje nigun campo vacio");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

