    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
            Lee leo = new Lee();
            String ec= (ecua.getText());

            String vain= (ini.getText());
            String susti, origigi;
            double fx=0;
            double reg = Double.parseDouble(ini2.getText());
            double set = Double.valueOf(vain)+ reg;
            String vain2 = String.valueOf(set);
            double tes = Double.valueOf(vain) - reg;
            String vain3 = String.valueOf(tes);

            if(forward.isSelected()==true)
            {

                origigi = leo.evaluar(ec, vain);
                System.out.println(leo.getResultadoConversion());
                susti = leo.evaluar(ec, vain2);
                la.setText("Valor Siguiente:  " + vain2);
                la1.setText("");
                fx= (Double.valueOf(susti) - Double.valueOf(origigi))/reg;
                res.setText(String.valueOf(fx));

            }

            if(backward.isSelected()== true)
            {

                la1.setText("");
                la.setText("Valor Anterior:  " + vain3);
                origigi = leo.evaluar(ec, vain);
                susti = leo.evaluar(ec, vain3);
                fx= (Double.parseDouble(origigi) - Double.parseDouble(susti))/reg;
                res.setText(String.valueOf(fx));
            }

            if(central.isSelected()==true)
            {
                la1.setVisible(true);
                la.setText("Valor Siguiente:  " + vain2);
                la1.setText("Valor Anterior:  " + vain3);

                origigi = leo.evaluar(ec, vain2);
                susti = leo.evaluar(ec, vain3);
                fx= (Double.parseDouble(origigi) - Double.parseDouble(susti))/(2*reg);
                res.setText(String.valueOf(fx));
            }

            if(centralder.isSelected()==true)
            {
                la1.setVisible(true);
                la.setText("Valor Siguiente:  " + vain2);
                la1.setText("Valor Anterior:  " + vain3);
                double centro = Double.valueOf(leo.evaluar(ec, vain));
                origigi = leo.evaluar(ec, vain2);
                susti = leo.evaluar(ec, vain3);
                fx= (Double.parseDouble(origigi)- (2*centro) + Double.parseDouble(susti))/(Math.pow(reg, 2));
                res.setText(String.valueOf(fx));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error.\nVerifique que su ecuacion sea correcta y no deje nigun campo vacio");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

