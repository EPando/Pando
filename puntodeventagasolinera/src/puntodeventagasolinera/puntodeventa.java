    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        String admincont=JOptionPane.showInputDialog(null,"Ingrese llave de Administrador");
        if (admincont.equals("mifechadenacimiento")==true)
        {
            String magnaactu=JOptionPane.showInputDialog(null,"Ingrese el aumento a la gasolina Magna");
            String premiumactu=JOptionPane.showInputDialog(null,"Ingrese el aumento a la gasolina Premium");
            String dieselactu=JOptionPane.showInputDialog(null,"Ingrese el aumento al diesel");
            magnaact=magnaact+Double.parseDouble(magnaactu);
            premiumact=premiumact+Double.parseDouble(premiumactu);
            dieselact=dieselact+Double.parseDouble(dieselactu);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Llave de administrador incorrecta ");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnactualizarActionPerformed

