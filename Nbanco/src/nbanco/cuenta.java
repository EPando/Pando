private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    
    int ji=0, ban = 0;
    String h = nucf.getText();
    String du = nof.getText();
    double entra = Double.parseDouble(mov.getText());
    nba.Nbanco(h, du);
   if(totalcuentas == 10)
    {
    ticket.setText(" No podemos registrar mas, solo son 10 registros");
    }
   else
   {
    while(ji<10)
    {
    
    if(h.equals(cuenta[ji])) 
    {
    ban=1;
    if(depret.getSelectedIndex()==0)
    {
    double noboby =nba.Nbanco(entra);    
    double saldo = Double.parseDouble(salado[ji]); 
    todo[ji]=nba.Nbanco();
    salado[ji]= String.valueOf(saldo+noboby);
    ticket.setText(todo[ji]+ "\n Saldo: "+salado[ji]);
    }
    if(depret.getSelectedIndex()==1)
    {
    double noboby = nba.Nbanco(entra);    
    double saldo = Double.parseDouble(salado[ji]); 
    todo[ji]=nba.Nbanco();
    salado[ji]= String.valueOf(saldo-noboby);
    ticket.setText(todo[ji]+ "\n Saldo: "+salado[ji]);
    }
    }
    ji++;
   
    }
    
    if(ban==0)
    {
    if(depret.getSelectedIndex()==0)
    {
    double noboby = nba.Nbanco(entra);    
    cuenta[totalcuentas]=h;
    nombre[totalcuentas]=du;
    todo[totalcuentas]=nba.Nbanco();
    salado[totalcuentas]= String.valueOf(noboby);
    ticket.setText(todo[totalcuentas]+ "\n Saldo: "+mov.getText());
    totalcuentas++;
    }
    if(depret.getSelectedIndex()==1)
    {
    ticket.setText(" Lo sentimos:"+" \n Este usuario no tiene cuenta en este banco.");
    todo[totalcuentas]="Usuario no registrado aun";
    cuenta[totalcuentas]="  ";
    salado[totalcuentas]="  ";
    totalcuentas++;
    }
    }
    
   }
                
}//GEN-LAST:event_jButton1ActionPerformed

