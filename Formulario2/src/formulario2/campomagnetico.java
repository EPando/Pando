    double ho, um,umo,ba, con, key, H3;
 
   NumberFormat nf = NumberFormat.getInstance();
    um= Double.parseDouble(mu.getText());
    umo = Double.parseDouble(muo.getText());
    ba=Double.parseDouble(ab.getText());
    con = Math.pow(10,-7)* Math.PI*umo;
    key = (1+((2*um)/con)*(2+(um/con)))-2*Math.pow(ba,3)+ Math.pow(((um/con)-1),2);
    
    nf.setMaximumFractionDigits(4);
    H3= (9*(um/con))/key;
    String go = nf.format(H3);
    res.setText(String.valueOf(go));
 
