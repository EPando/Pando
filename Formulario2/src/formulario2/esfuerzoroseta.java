private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    double a, b, e, ka, q, mu,q2,q3, exp;
 
    NumberFormat nf = NumberFormat.getInstance();
    exp = Double.parseDouble(ex.getText());
    mu= Double.parseDouble(um.getText());
    q = Double.parseDouble(Qi.getText());
    q3=Double.parseDouble(Q3.getText());
    q2=Double.parseDouble(Q2.getText());
    e = Double.parseDouble(E.getText());
    ka = Double.parseDouble(k.getText());
    
    a = ((Math.pow(10,exp)*e)*(1-ka)/(2*(1-mu)))*(q+q3);
    nf.setMaximumFractionDigits(2);
    String pr = nf.format(a);   
    b = (((Math.pow(10,exp)*e)*(1-ka))/(2*(1-mu))) * Math.sqrt(Math.pow(q-q3,2) + Math.pow(q2-q3,2));
    String prf = nf.format(b);
    A.setText(String.valueOf(pr));
    B.setText(String.valueOf(prf));
    double mas=a + b;
    double menos = a - b;
    String sam = nf.format(mas);
    String sonem = nf.format(menos);
    res.setText(String.valueOf(sam));
    smin.setText(String.valueOf(sonem));
}//GEN-LAST:event_jButton1ActionPerformed

