private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
// TODO add your handling code here:
    double res1,res2,res3,res4,h1,h2,h3,h4, ri, avi, del, rl;
    int exp, ex;
    double ds, d;
    
    NumberFormat nf = NumberFormat.getInstance();
    exp = Integer.parseInt(e1.getText());
    ds = Math.pow(10, -exp);
    ex = Integer.parseInt(e2.getText());
    d = Math.pow(10, -ex);
    res1 = Math.pow(Double.parseDouble(p.getText()), -1);
    res2 = Math.pow(Double.parseDouble(k.getText()),-1);
    res3 = Math.pow(Double.parseDouble(n.getText()), -1); 
    res4 = Math.pow(Double.parseDouble(q.getText()),-1);
    rl= Math.pow((res1 + res2 + res3 + res4), -1);
    h1 = Double.parseDouble(p1.getText());
    h2 = Double.parseDouble(p2.getText());
    h3 = Double.parseDouble(p3.getText())*ds;
    h4 = Double.parseDouble(p4.getText()) * d;
    del = (h1*h4) - (h2*h3);
    ri = (h1 + ((del)*rl))/(1 + (rl*h4));
    avi = (-(h2*rl))/(-h1 + (del*rl));
    
    nf.setMaximumFractionDigits(2);
    String av = nf.format(avi);
    String roi = nf.format(ri);
    r1.setText(String.valueOf(roi));
    r.setText(String.valueOf(av));
    
}//GEN-LAST:event_jButton3ActionPerformed

