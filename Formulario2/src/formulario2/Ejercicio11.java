q=Double.parseDouble(caja3.getText());
ra=Double.parseDouble(caja4.getText());
re=Double.parseDouble(caja5.getText());
otro = 2 * 3.1416 * f;
    c = 1 / ((Math.pow(otro, 2)) * l);
    rp = Math.pow(q, 2) * re;
