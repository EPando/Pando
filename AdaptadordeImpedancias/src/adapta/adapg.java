     double c, frec;
     double la, lb, dis1, dis2, ld,lbd;
     frec= Double.parseDouble(frecc.getText());
     lbd=300/frec;   
      dis1= Double.parseDouble(d1.getText()); 
      dis2=dis1/3;
      la=lbd;
      lb=(lbd*10)/100;
      ld=lbd/70;
      c=8*lbd;
      
      nf.setMaximumFractionDigits(3);
            
