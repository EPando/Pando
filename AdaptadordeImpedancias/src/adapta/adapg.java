            String m = nf.format(la);
            String s = nf.format(lb);
            String t = nf.format(ld);
            String z = nf.format(dis2);
            cap.setText(String.valueOf(al+" pf"));
            d2.setText(String.valueOf(z+" m"));
            aa.setText(String.valueOf(m+" m"));
            b.setText(String.valueOf(s+" m"));
            d.setText(String.valueOf(t+" m"));
    
    
